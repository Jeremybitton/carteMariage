var envelopeOverlay = document.getElementById('envelopeOverlay');
var envelope = document.getElementById('envelope');
var instruction = document.getElementById('instruction');
var mainSite = document.getElementById('mainSite');

if (envelopeOverlay && envelope) {
    var isOpening = false;

    envelopeOverlay.addEventListener('click', function () {
        if (isOpening) return;
        isOpening = true;

        // 1. Ouvre l'enveloppe
        envelope.classList.add('opened');
        if (instruction) instruction.style.opacity = '0';

        // Lancement de la musique
        var bgMusic = document.getElementById('bg-music');
        var musicIcon = document.getElementById('music-icon');
        var musicBtn = document.getElementById('music-btn');
        if (bgMusic) {
            bgMusic.play().then(function () {
                if (musicIcon) {
                    musicIcon.classList.remove('bi-volume-mute-fill');
                    musicIcon.classList.add('bi-volume-up-fill');
                }
                if (musicBtn) {
                    musicBtn.classList.remove('muted');
                }
            }).catch(function (err) {
                console.log("Lecture audio bloquée ou non trouvée", err);
            });
        }

        // 2. Sort la carte
        setTimeout(function () {
            envelope.classList.add('pull-out');
        }, 800);

        // 3. Zoom sur la carte
        setTimeout(function () {
            envelope.classList.add('zooming');
        }, 1500);

        // 4. Disparition de l'enveloppe + apparition du faire-part
        setTimeout(function () {
            // Masque l'overlay
            envelopeOverlay.style.display = 'none';
            // Déverrouille le scroll (technique iOS)
            unlockScroll();
            // S'assure qu'on est bien en haut
            window.scrollTo(0, 0);
            // Révèle le site
            if (mainSite) mainSite.classList.add('revealed');
        }, 2200);
    });
}

